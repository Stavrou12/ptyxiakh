    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setCharacterEncoding("UTF-8");
       response.setContentType("application/json; charset=UTF-8");
  
         String subject = request.getParameter("subject");
        String email = request.getParameter("contactEmail");
 
         

        String message = request.getParameter("contactMessage");


        // Call the EmailSender class to send the email
        boolean emailSent = SendEmail.sendEmail(email, subject, message);

        // Send a JSON response back to the client
         String jsonResponse = "{\"message\": \"" + (emailSent ? "Email was sent" : "Failed to send email") + "\"}";
         response.getWriter().write(jsonResponse);
    }   
    
    
    
